package com.cesurazure.crm.model;

public class Solution {

    private int id;
    private String solutionId;
    private String solutionTitle;
    private String solutionOwner;
    private String solutionOwnerEmail;
    private String solutionOwnerMobile;
    private String solutionStatus;
    private String solutionDescription;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSolutionId() {
        return solutionId;
    }

    public void setSolutionId(String solutionId) {
        this.solutionId = solutionId;
    }

    public String getSolutionTitle() {
        return solutionTitle;
    }

    public void setSolutionTitle(String solutionTitle) {
        this.solutionTitle = solutionTitle;
    }

    public String getSolutionOwner() {
        return solutionOwner;
    }

    public void setSolutionOwner(String solutionOwner) {
        this.solutionOwner = solutionOwner;
    }

    public String getSolutionOwnerEmail() {
        return solutionOwnerEmail;
    }

    public void setSolutionOwnerEmail(String solutionOwnerEmail) {
        this.solutionOwnerEmail = solutionOwnerEmail;
    }

    public String getSolutionOwnerMobile() {
        return solutionOwnerMobile;
    }

    public void setSolutionOwnerMobile(String solutionOwnerMobile) {
        this.solutionOwnerMobile = solutionOwnerMobile;
    }

    public String getSolutionStatus() {
        return solutionStatus;
    }

    public void setSolutionStatus(String solutionStatus) {
        this.solutionStatus = solutionStatus;
    }

    public String getSolutionDescription() {
        return solutionDescription;
    }

    public void setSolutionDescription(String solutionDescription) {
        this.solutionDescription = solutionDescription;
    }

   
}
